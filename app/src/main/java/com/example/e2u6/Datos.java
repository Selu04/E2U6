package com.example.e2u6;

public class Datos {
    private String pais;
    private String capital;

    public Datos(String pais, String capital) {
        this.pais = pais;
        this.capital = capital;
    }

    public String getPais() {
        return pais;
    }

    public String getCapital() {
        return capital;
    }
}
